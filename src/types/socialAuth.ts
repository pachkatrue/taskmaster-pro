/**
 * Типы для авторизации через социальные сети
 * Содержит интерфейсы и типы, необходимые для работы с социальными авторизациями
 */

/**
 * Поддерживаемые провайдеры социальной авторизации
 */
export type SocialProvider = 'google' | 'facebook';

/**
 * Структура данных пользователя, получаемая от провайдера
 */
export interface SocialUser {
  id: string;                 // Уникальный идентификатор пользователя в системе провайдера
  fullName: string;           // Полное имя пользователя
  email: string;              // Email пользователя
  avatar?: string;            // URL аватара пользователя (опционально)
  provider: SocialProvider;   // Провайдер, через который произошла авторизация
}

/**
 * Результат успешной авторизации через социальную сеть
 */
export interface SocialAuthResult {
  user: SocialUser;           // Данные пользователя
  token: string;              // Токен доступа для API
}

/**
 * Структура ошибки авторизации через социальную сеть
 */
export interface SocialAuthError {
  code: string;               // Код ошибки
  message: string;            // Сообщение об ошибке
  provider: SocialProvider;   // Провайдер, при авторизации через который произошла ошибка
}